export function findRelevantKnowledge(query: string): string {
  const lower = query.toLowerCase()
  const results: string[] = []

  if (lower.includes("закуп") || lower.includes("контракт") || lower.includes("тендер")) {
    results.push("Портал ЕИС: zakupki.gov.ru | Анализ: clearspending.ru")
    results.push("Признаки нарушений: " + CIVIC_KNOWLEDGE.budget_control.red_flags.slice(0, 3).join("; "))
  }

  if (lower.includes("жкх") || lower.includes("управляю") || lower.includes("тариф") || lower.includes("капрем")) {
    results.push("ГИС ЖКХ: dom.gosuslugi.ru | ПП РФ №354")
    results.push("Что контролировать: " + CIVIC_KNOWLEDGE.housing_control.what_to_control.slice(0, 3).join("; "))
  }

  if (lower.includes("запрос") || lower.includes("информац") || lower.includes("8-фз") || lower.includes("59-фз")) {
    results.push("Срок ответа по 8-ФЗ и 59-ФЗ: 30 дней")
  }

  if (lower.includes("эко") || lower.includes("свалк") || lower.includes("загряз") || lower.includes("отход")) {
    results.push("Жалобы: Росприроднадзор (rpn.gov.ru), Роспотребнадзор")
    results.push("Законы: 7-ФЗ, 89-ФЗ, 52-ФЗ")
  }

  if (lower.includes("прокурат") || lower.includes("жалоб") || lower.includes("нарушен")) {
    results.push("Портал прокуратуры: epp.genproc.gov.ru (электронное обращение)")
  }

  return results.join("\n")
}
